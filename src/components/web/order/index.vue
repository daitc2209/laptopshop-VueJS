<template>
<div>
	<head><title>Đặt hàng</title></head>
  <section class="order">
			<div class="container">
				<form class="order-form" @submit.prevent="Order()">
					<div class="row">
						<div class="breadcrumbs d-flex flex-row align-items-center col-12">
							<ul>
								<li><a href="/home">Trang chủ</a></li>
								<li class="active"><a href="#"><i
										class="fa fa-angle-right" aria-hidden="true"></i>Đặt hàng</a></li>
							</ul>
						</div>
						<div class="col-lg-6">
							<div class="information-detail">
								<h4>Thông tin đơn hàng</h4>
								<div class="col-12 mb-2 border info-user p-3">
									<div class="col-lg-12 d-flex align-items-center">
										<label>Họ và Tên : </label> <p>{{userLogin.fullname}}</p>
									</div>
									<div class="col-lg-12 d-flex align-items-center">
										<label>Email : </label> <p>{{userLogin.email}}</p>
									</div>
									<div class="col-lg-12 d-flex align-items-center">
										<label>Tổng tiền : </label> <p>{{ formatCurrency(totalMoney) }}</p>
									</div>
									<div class="col-lg-12 d-flex align-items-center">
										<label>Ngày đặt : </label> <p>{{ formatDate(orderDate) }}</p>
									</div>
								</div>
								<div class="col-12  border info-order p-3">
									<div class="col-lg-12  pb-4 ">
										<label>SĐT <span>*</span></label> 
										<input type="text" v-model="OrderRequest.phone" name="phone" required="required" />
									</div>
									<div class="col-lg-12  pb-4">
										<label>Địa chỉ nhận hàng <span>*</span></label> 
										<input type="text" v-model="OrderRequest.address" name="address" required="required" />
									</div>
									<div class="col-lg-12  pb-4">
										<label>Ghi chú <span>*</span></label> 
										<textarea name="note" v-model="OrderRequest.note"  id="floatingTextarea2" class="form-control" style="height: 70px;"></textarea>
									</div>
									<div class="mt-sm-1 ">
										<label class="fw-bold text-size-18 d-block">Vận chuyển <span>*</span></label> 
										<div class="d-flex justify-content-between align-items-center mb-4 ">
											<div class="text-size-14 mt-1">
												<input type="radio" checked style="width: 10px; height: 10px; margin-right: 4px;" />
												<label style="align-items: center;">Giao hàng tận nơi</label>
											</div>
											<span class="fw-bold text-size-14">40.000 đ</span><br>
										</div>
									</div>
									<div class="col-lg-12 ">
										<label>Hình thức thanh toán <span>*</span></label> 
										<select class="form-control form-select" @change="typePAY()" name="typePayment" id="drTypePayment" required="required">
											<option value="COD">Thanh toán khi nhận hàng (COD)</option>
											<option value="TRANSFER">Chuyển khoản</option>
										</select>
										<div class="form-group payment mt-2" id="load-payment" @change="typeBankcode()">
											<p>Chọn phương thức thanh toán</p>
											
											<p>Cách 1: Chuyển hướng sang Cổng VNPAY chọn phương thức thanh toán</p>
											<input type="radio" Checked="checked" id="bankCode" name="bankCode" value="NCB"> 
											<label for="bankCode">Cổng thanh toán VNPAYQR</label><br>

											<p>Cách 2: Tách phương thức tại site của đơn vị kết nối</p>
											<input type="radio" id="bankCode1" name="bankCode" value="VNPAYQR"> 
											<label for="bankCode1">Thanh toán bằng ứng dụng hỗ trợ VNPAYQR</label>
											<br> 
											<input type="radio" id="bankCode2" name="bankCode" value="VNBANK">
											<label for="bankCode2">Thanh toán qua thẻ ATM/Tài khoản nội địa</label>
											<br> 
											<input type="radio" id="bankCode3" name="bankCode" value="INTCARD"> 
											<label for="bankCode3">Thanh toán qua thẻ quốc tế</label><br>
										</div>
									</div>
								</div>
							</div>
						</div>
						<div class="col-lg-6">
							<div class="place-order">
								<h4>Thông tin sản phẩm</h4>
								<div class="order-total">
									<ul class="order-table p-0">
										<li><span>Sản phẩm</span><span>Số lượng</span><span>Giá</span><span>Discount</span><span>Tổng</span></li>
										<li class="fw-normal" v-for="(item, index) in listCart" :key="index">
												<span><img :src="`/src/images/product/`+item.img" style="width:50px; height:50px" alt="Product Image" /></span>
												<span>{{item.numProduct}}</span>
												<span>{{ formatCurrency(item.price) }}</span>
												<span>{{ item.discount }}%</span>
												<span>{{ formatCurrency(item.totalPrice) }}</span>
										</li>
										<li class="total-price">Tổng số lượng <p>{{totalQuantity}}</p></li>
										<li class="total-price">Tổng tiền <p>{{formatCurrency(totalMoney)}}</p></li>
										<li class="total-price">Phí vận chuyển <p>{{formatCurrency(40000)}}</p></li>
										<li class="total-price">Thành tiền <p>{{ formatCurrency(totalMoney + 40000) }}</p></li>
									</ul>
									<div class="order-btn">
										<button type="submit" class="site-btn place-btn">Đặt hàng</button>
									</div>
								</div>
							</div>
						</div>
					</div>
				</form>
			</div>
		</section>
	</div>
</template>

<script>
import Order from '../../../service/Order'
export default {
	data(){
		return {
			userLogin: {
				fullname:" ",
				email: ""
			},
			totalMoney: '',
			totalQuantity:'',
			orderDate: "",
			listCart: [],
			OrderRequest:{
				typePayment: 'COD',
				phone:'',
				address:'',
				bankCode:'' ,
				note:''
			}
		}
	},
	methods: {
		formatDate(date) {
			const formattedDate = new Date(date);
			const hours = ('0' + formattedDate.getHours()).slice(-2);
			const minutes = ('0' + formattedDate.getMinutes()).slice(-2);
			const day = ('0' + formattedDate.getDate()).slice(-2);
			const month = ('0' + (formattedDate.getMonth() + 1)).slice(-2);
			const year = formattedDate.getFullYear();
			return `${hours}:${minutes} ${day}/${month}/${year}`;
		},
		formatCurrency(value) {
			const formatter = new Intl.NumberFormat("vi-VN", {
			style: "currency",
			currency: "VND",
			});
			return formatter.format(value);
		},
		typePAY(){
			let type = document.getElementById('drTypePayment').value;
			this.OrderRequest.typePayment = type;
			console.log("type: " + this.OrderRequest.typePayment);

			// Hiển thị hoặc ẩn phần tử "#load-payment" dựa trên giá trị của "type"
			if (type === "TRANSFER") {
				$("#load-payment").show();
			} else {
				$("#load-payment").hide();
			}
		},
		typeBankcode(){
			let bank = document.querySelector("input[name='bankCode']:checked").value
			this.OrderRequest.bankCode = bank;
			return this.OrderRequest.bankCode;
		},
		getOrder(){
			Order.getOrder()
				.then((res)=>{
					this.userLogin.email = res.data.data.email
					this.userLogin.fullname = res.data.data.fullname
					this.listCart = res.data.data.listCart
					this.orderDate = res.data.data.orderDate
					this.totalQuantity = res.data.data.totalQuantity
					this.totalMoney = res.data.data.totalMoney
					console.log("date: "+this.orderDate)

				})
				.catch((err)=>{
					console.log("err order: "+err)
				})
		},
		Order(){
			if (this.OrderRequest.typePayment == "TRANSFER")
				this.OrderRequest.bankCode = this.typeBankcode();
			console.log("bank: "+this.OrderRequest.bankCode);
			console.log("type: "+this.OrderRequest.typePayment);


			Order.postOrder(this.OrderRequest)
				.then((res)=>{
					console.log("gui order thanh cong: "+res)
					console.log("data res: "+res.data)
					
						window.location.href = res.data.data.redirectUrl

						if(res.data.data.typePayment == "COD")
						{
							sessionStorage.setItem("typePayment",res.data.data.typePayment);
							sessionStorage.setItem("orderId",res.data.data.orderId);
						}
					console.log("TRANSFER !!!!")
				})
				.catch((err)=>{
					console.log("err order: "+err)
				})
		}
	},
	mounted(){
		if (sessionStorage.getItem("login"))
		{
			this.getOrder()
		}
		else
		{
			window.location.href = '/auth/sign-in'
			sessionStorage.setItem("err", true)
		}
	}
}
</script>

